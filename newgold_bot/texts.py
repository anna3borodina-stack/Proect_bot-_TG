def _trim_url(url: str | None) -> str:
    return url.strip() if url else ""


def start_welcome(store_url: str | None) -> str:
    """Текст /start до согласия с политикой."""
    base = (
        "Здравствуйте! Вы в помощнике бренда NEWGOLD — ювелирные украшения ✨\n\n"
        "Мы подскажем по заказу и доставке, камням и оттенкам золота, проведём короткую консультацию "
        "и при необходимости передадим Вас менеджеру. Общение — на Вы.\n\n"
        "Перед началом ознакомьтесь с политикой обработки персональных данных "
        "и подтвердите согласие кнопкой ниже."
    )
    u = _trim_url(store_url)
    if u:
        base += f"\n\n🛒 Интернет-магазин: {u}"
    return base


def after_consent(store_url: str | None) -> str:
    """Текст сразу после согласия."""
    lines = [
        "Спасибо! Выберите раздел в меню ниже или пройдите консультацию — "
        "мы зададим несколько вопросов и поможем сформулировать запрос 💬",
    ]
    u = _trim_url(store_url)
    if u:
        lines.append(f"\nКаталог и оформление заказа: {u}")
    lines.append(
        "\n\nБыстрый переход на витрину: кнопка «🌐 Сайт» или команда /site."
        "\nКоманды: /help — справка, /manager — заявка, /cancel — выход из сценария."
    )
    return "".join(lines)
